
public class Anas_Mondol {
  
    public static void main(String[] args) {
        //created variable.
        double a;
        a = 20.90;
        System.out.println("The double value : "+a);
        float c;
        c = 21.91F;
        System.out.println("The floating point value : "+c);
        long e;
        e = 78;
        System.out.println("The long value : "+e);
        int g;
        g = 22;
        System.out.println("The integer value : "+g);
                               
        //converted variablae.
        byte b =(byte)a;
        System.out.println("\nConverted double to byte value : "+b);
        short d =(short)c;
        System.out.println("Converted float to short value : "+d);
        char f =(char)e;
        System.out.println("Converted long to character value : "+f);
        byte h =(byte)g;
        System.out.println("Converted integer to byte value : "+h);
                   
    }
    
}
