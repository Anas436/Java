
public class Anas_Mondol {
   
    public static void main(String[] args) {
       // created variable.
        byte a = 10;
        System.out.println("The byte value : "+a);
        short c = 11;
        System.out.println("The short value : "+c);
        char e = 'A' ;
        System.out.println("The character value : "+e);
        int g = 12;
        System.out.println("The integer value : "+g);
        long i = 13;
        System.out.println("The long value : "+i);
                                
        // converted variable.
        int b =(int)a;
        System.out.println("\nConverted byte to integer value : "+ b);
        double d =(double)c;
        System.out.println("Converted short to double value : "+d);
        int f =(int)e;
        System.out.println("Converted character to integer value : "+f);
        float h =(float)g;
        System.out.println("Converted integer to floating point value : "+h);
        double j =(double)i;
        System.out.println("Converted long to double value : "+j);                        
                        
    }
    
}
